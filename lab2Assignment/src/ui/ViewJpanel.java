    private void populateTable() {
        // facilitate the manipulation of the table
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
       //setting row count 
       model.setRowCount(0);
       
       for(VitalSigns vs: history.getHistory()){
           Object[] row = row = new Object[3];
           row[0] = vs;
           row[1] = vs.getTemperature();
           row[2] = vs.getPulse();
           
           model.addRow(row);
       }
       
    }
